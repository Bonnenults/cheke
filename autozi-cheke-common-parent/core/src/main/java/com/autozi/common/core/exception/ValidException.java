package com.autozi.common.core.exception;

public class ValidException extends BaseException {
	private static final long serialVersionUID = -5523728792277194566L;

	public ValidException() {
		super();
	}

	public ValidException(String message, Throwable cause) {
		super(message, cause);
	}

	public ValidException(String message) {
		super(message);
	}

	public ValidException(Throwable cause) {
		super(cause);
	}
	
}
